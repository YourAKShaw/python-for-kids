# Dictionaries in Python

'''
Dictionaries are a collection of key-value pairs.
'''

# dictionary for day number to day
days = { 
        1: 'Monday', 
        2: 'Tuesday', 
        3: 'Wednesday', 
        4: 'Thursday', 
        5: 'Friday', 
        6: 'Saturday', 
        7: 'Sunday' 
    }

print(days[5])

# In the above statement, we are accessing the value of the key 5.
# If we take the analogy of a traditional dictionary, we can say that 
# days is our dictionary, 5 is word and 'Friday' is the meaning.